// double ended queues 
/*
Don't confuse these with dequeue() function in queues.
DEqueue stands for double ended queues.

In thi, FIFO is not followed, as in the normal queues.
Here unlike the normal queues, insertion and deletion can be done from both front and rear ends

2 types of DEqueue exists - 
    1. Restricted input - insertion from front is not allowed
    2. Restricted output - deletion from the rear is not allowed.

These are very similar to the normal queues, just the additional feature in them include insertion of a node in the front and deletion of a node from the rear.
*/