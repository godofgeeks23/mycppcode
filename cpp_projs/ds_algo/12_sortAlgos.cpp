// sorting algorithms
/*
Analysis Criteria for sorting algos - 
    1.  Time complexity
    2.  Space complexity - 
            Inplace sorting algos - they always use constant memory for any size of the input. This is generally achieved by the use of swapping in the process
    3.  Stability - order of equal elements in sequence must be maintained.
            Example - 6*,1,2,6 --(after sorting)--> 1,2,6*,6            (this is how a stable algo works)
    4.  Internal sorting algo - all data is laoded into the memory
    5.  External Sorting Algo - not all data is loaded
    6.  Adaptive - alreday sorted data takes less time
    7.  Recursive vs Non Recursive
*/